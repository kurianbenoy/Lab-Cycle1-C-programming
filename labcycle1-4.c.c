int main(int argc, char *argv[])
{
   	int n,i=1;
	printf("Enter a no");
	scanf("%d",&n);
	float sum=0;
	while (i<=n)	
	{
		sum+=pow(-1,i+1)/i;
		i+=1;
}
	printf("The sum is %f",sum);
	return 0;
}
